package com.niit.petshop;

public class UsersTable {
	
	public int user_id; 			//primary key
	private String user_firstname;
	private String user_lastname;
	private String user_email;
	private double user_mobilenumber;
	private String user_address;
	private String user_city;
	private double user_pincode;
	private String user_country;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getUser_firstname() {
		return user_firstname;
	}
	public void setUser_firstname(String user_firstname) {
		this.user_firstname = user_firstname;
	}
	public String getUser_lastname() {
		return user_lastname;
	}
	public void setUser_lastname(String user_lastname) {
		this.user_lastname = user_lastname;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public double getUser_mobilenumber() {
		return user_mobilenumber;
	}
	public void setUser_mobilenumber(double user_mobilenumber) {
		this.user_mobilenumber = user_mobilenumber;
	}
	public String getUser_address() {
		return user_address;
	}
	public void setUser_address(String user_address) {
		this.user_address = user_address;
	}
	public String getUser_city() {
		return user_city;
	}
	public void setUser_city(String user_city) {
		this.user_city = user_city;
	}
	public double getUser_pincode() {
		return user_pincode;
	}
	public void setUser_pincode(double user_pincode) {
		this.user_pincode = user_pincode;
	}
	public String getUser_country() {
		return user_country;
	}
	public void setUser_country(String user_country) {
		this.user_country = user_country;
	}
	
	
	

}
